# Импортируем стандартные модули
import math
from datetime import datetime

# Используем функцию sqrt() для вычисления квадратного корня
number = 16
square_root = math.sqrt(number)
print(f"Квадратный корень числа {number} равен {square_root}")

# Отображаем текущую дату и время
current_time = datetime.now()
print(f"Текущая дата и время: {current_time}")
