# Задача 1: Программа, которая запрашивает у пользователя ввод числа и выводит все числа от 1 до введенного числа включительно
num = int(input("Введите число: "))
for i in range(1, num + 1):
    print(i)

# Задача 2: Программа, которая запрашивает у пользователя ввод 2 чисел и выводит большее из них
first_num = int(input("Введите первое число: "))
second_num = int(input("Введите второе число: "))

if first_num > second_num:
    print("Большее число:", first_num)
else:
    print("Большее число:", second_num)
